                    <h2 class="blog_title"><a href="blog-single.html"> C.U.P. Through the years...</a></h2>
                    
                    <p>The Pamantasan Ng Lungsod Ng Pasay (PLP) was founded on May 26, 1994 by virtue of City Ordinance No. 3939, as amended by City Ordinance No. 4155. The Constitution was conceptualized by the late Mayor Hon. Pablo N. Cuneta, and the city councilors. Its major purpose was to provide quality education at a minimal cost to the less privileged but deserving students of the city and to some others who can meet certain admission requirements set forth by the Board of Regents. Pamantasan Ng Lungsod Ng Pasay (PLP) has been renamed “City University of Pasay” (CUP), by virtue of an amendment made in a year 2011 (City Ordinance No. 4604, S-2011). The latest of the series of amendment to CUP Charter is Ordinance No. 5363, series of 2013 which amended Section 1&amp; 2 of Article 3; Section 3 of Article4 of Ordinance No. 4604, Series of 2011. Hereunder are the amendments made over the period of 20 years on the CUP Charter.

                    <ul>
                      <li>   City Ordinance No. 271, Series of 1994 - Establishment of Pamantasan ng Lungsod ng Pasay</li>
                      <li>   City Ordinance No. 3939, Series 2007 - Revised Charter of Pamantasan ng Lungsod ng Pasay (First Revision)</li>
                      <li>   City Ordinance No. 4155, Series of 2008 – Revised Charter of Pamantasan ng Lungsod ng Pasay (Second Revision)</li>
                      <li>   City Ordinance No. 4604, Series of 2011- Revised Charter of Pamantasan ng Lungsod ng Pasay adopting a new name “City University of Pasay”,</li>
                      <li>   City Ordinance No. 5362 S- 2013</li>
                    </ul>

                       The declaration of policy of Ordinance 5362 states: “it shall hereby be declared the policy of the City of Pasay
                    to be genuinely committed, in consonance with the policy of the State, to promote the right of all citizens to quality education at all levels and to make such education accessible to all. Moreover, the City of Pasay, aware of its obligations to give poor but deserving, talented and gifted students of the City, the opportunity to develop their potentialities, is committed to establish tertiary education.

                       At the outset, the school started with 384 students and 11 faculty members, holding evening classes at the Pasay
                    City West High School. Dr.Felimon Salas and his staff at the guidance research center composed the
                    working group for the university which paved the way toward the recognitionof the unive rsity as an institution of higher learning.

                       The Division of City Schools, Pasay City played a significant role in the development of the Pamantasan
                    providing not only the consultative services but also personnel services including the use of school facilities such as buildings, classrooms, desks, boards and to some extent supplies and other materials and equipment for instructional purposes.

                    Today the CUP offers degree programs in areas of teacher education, Law, business education, nursing and health
                    education, hotel and restaurant management, Computer education and Graduate Studies.

                        The School of Law is composed of young and dynamic law professors with strong dedication and commitment to
                    produce new lawyers for the country.

                        The School of Graduate Studies offers two graduate degree programs in educational management and public
                    governance. The college serves as the research center maximizing the use of the faculty resources, data banking and research forums. The graduate school Faculty are holders of doctorate degree in educational management.

                        Today CUP looks forward to achieving the objectives of the city and the tertiary education as well as working
                    toward accreditation.</p>

